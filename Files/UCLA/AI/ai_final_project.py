def embed_conversations(data):
    """
    data: list[ConvExample]
    returns:
        msg_embs: (num_convs, SEQ_LEN, embed_dim)
        regimes: (num_convs, SEQ_LEN)
    """
    all_texts = []
    idx_map = []  # (conv_index, step)
    for ci, conv in enumerate(data):
        for t, msg in enumerate(conv.messages):
            all_texts.append(msg)
            idx_map.append((ci, t))

    embs = []
    BATCH = 32
    for i in range(0, len(all_texts), BATCH):
        batch_texts = all_texts[i:i+BATCH]
        emb_batch = encode_text_batch(batch_texts)
        embs.append(emb_batch.cpu())
    embs = torch.cat(embs, dim=0)  # (total_msgs, embed_dim)

    num_convs = len(data)
    msg_embs = torch.zeros(num_convs, SEQ_LEN, embed_dim)
    regimes = torch.zeros(num_convs, SEQ_LEN, dtype=torch.long)

    for idx, (ci, t) in enumerate(idx_map):
        msg_embs[ci, t] = embs[idx]
        regimes[ci, t] = data[ci].regimes[t]

    return msg_embs, regimes

