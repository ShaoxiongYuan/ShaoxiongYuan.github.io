class SSMRegimeModel(nn.Module):
    def __init__(self, input_dim, state_dim, num_regimes):
        super().__init__()
        self.state_dim = state_dim
        self.num_regimes = num_regimes

        self.ssm = NeuralSSM(state_dim=state_dim, input_dim=input_dim, num_regimes=num_regimes)
        self.controller = RegimeController(state_dim=state_dim, input_dim=input_dim, num_regimes=num_regimes)

        self.h0 = nn.Parameter(torch.zeros(state_dim))

    def forward(self, seq_embs, seq_regimes=None):
        """
        seq_embs: (batch, T, input_dim)
        seq_regimes: (batch, T) or None
        returns:
            logits_seq: (batch, T, num_regimes)
        """
        batch_size, T, D = seq_embs.shape
        device = seq_embs.device

        h = self.h0.unsqueeze(0).expand(batch_size, -1)  # (batch, state_dim)
        logits_list = []

        for t in range(T):
            x_t = seq_embs[:, t, :]  # (batch, D)

            # Predict regime from (h_{t-1}, x_t)
            logits_t = self.controller(h, x_t)  # (batch, num_regimes)
            logits_list.append(logits_t.unsqueeze(1))

            if seq_regimes is None:
                # During inference we could sample / argmax, but for training we use teacher forcing
                z_t = logits_t.argmax(dim=-1)
            else:
                z_t = seq_regimes[:, t]

            # Update state
            h = self.ssm(h, x_t, z_t)

        logits_seq = torch.cat(logits_list, dim=1)  # (batch, T, num_regimes)
        return logits_seq

def train_ssm_model(
    train_embs, train_regimes,
    test_embs, test_regimes,
    state_dim=64,
    num_epochs=15,
    lr=1e-3
):
    model = SSMRegimeModel(
        input_dim=embed_dim,
        state_dim=state_dim,
        num_regimes=NUM_REGIMES
    ).to(device)

    opt = optim.Adam(model.parameters(), lr=lr)
    crit = nn.CrossEntropyLoss()

    history = {"train_loss": [], "test_acc": []}

    for epoch in range(num_epochs):
        model.train()
        x_train = train_embs.to(device)      # (N, T, D)
        y_train = train_regimes.to(device)   # (N, T)

        opt.zero_grad()
        logits_seq = model(x_train, seq_regimes=y_train)  # (N, T, K)
        loss = crit(
            logits_seq.view(-1, NUM_REGIMES),
            y_train.view(-1)
        )
        loss.backward()
        opt.step()

        # Eval
        model.eval()
        with torch.no_grad():
            x_test = test_embs.to(device)
            y_test = test_regimes.to(device)
            logits_test_seq = model(x_test, seq_regimes=y_test)
            preds = logits_test_seq.argmax(dim=-1)
            acc = (preds == y_test).float().mean().item()

        history["train_loss"].append(loss.item())
        history["test_acc"].append(acc)

        print(f"[SSM] Epoch {epoch+1}/{num_epochs} - "
              f"loss={loss.item():.4f} - test_acc={acc*100:.2f}%")

    return model, history

