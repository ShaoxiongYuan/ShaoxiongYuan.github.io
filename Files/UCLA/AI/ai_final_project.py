def build_chat_messages(history, user_message, regime_idx):
    """
    history: list[(role, text)] where role is "user" or "assistant"
    user_message: current user text
    regime_idx: int, index into REGIME_NAMES
    """
    mode = REGIME_NAMES[regime_idx]  # EXPLAINER / TUTOR / CONCISE

    system_content = {
        "EXPLAINER": (
            "You are a helpful AI assistant. "
            "Current mode: EXPLAINER. Give detailed multi-sentence explanations."
        ),
        "TUTOR": (
            "You are a patient step-by-step tutor. "
            "Current mode: TUTOR. Answer in numbered steps with clear reasoning."
        ),
        "CONCISE": (
            "You are a concise assistant. "
            "Current mode: CONCISE. Answer in one or two short sentences only."
        )
    }[mode]

    messages = [
        {"role": "system", "content": system_content}
    ]

    for role, text in history:
        if role == "user":
            messages.append({"role": "user", "content": text})
        else:
            messages.append({"role": "assistant", "content": text})

    # current user turn
    messages.append({"role": "user", "content": user_message})
    return messages


def generate_chat_response(history, user_message, regime_idx, max_new_tokens=128, temperature=0.7):
    """
    Use the advanced chat LLM with its chat template.
    """
    messages = build_chat_messages(history, user_message, regime_idx)

    input_ids = gen_tokenizer.apply_chat_template(
        messages,
        return_tensors="pt",
        add_generation_prompt=True   # tells model it's assistant's turn
    ).to(device)

    with torch.no_grad():
        output_ids = gen_model.generate(
            input_ids,
            max_new_tokens=max_new_tokens,
            do_sample=True,
            temperature=temperature,
            pad_token_id=gen_tokenizer.eos_token_id,
        )

    # Decode only the newly generated tokens (after the prompt)
    gen_tokens = output_ids[0, input_ids.shape[-1]:]
    response = gen_tokenizer.decode(gen_tokens, skip_special_tokens=True).strip()
    return response

