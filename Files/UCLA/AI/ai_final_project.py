class RegimeController(nn.Module):
    """
    Predicts regime z_t from:
    - previous hidden state h_{t-1}
    - current message embedding x_t
    """
    def __init__(self, state_dim: int, input_dim: int, num_regimes: int):
        super().__init__()
        self.num_regimes = num_regimes
        self.net = nn.Sequential(
            nn.Linear(state_dim + input_dim, 128),
            nn.ReLU(),
            nn.Linear(128, num_regimes)
        )

    def forward(self, h_prev: torch.Tensor, x_t: torch.Tensor):
        """
        h_prev: (batch, state_dim)
        x_t: (batch, input_dim)
        returns:
            logits: (batch, num_regimes)
        """
        inp = torch.cat([h_prev, x_t], dim=-1)
        logits = self.net(inp)
        return logits

class NeuralSSM(nn.Module):
    """
    Neural state-space model:
        h_t = f(h_{t-1}, x_t, one_hot(z_t))
    """
    def __init__(self, state_dim: int, input_dim: int, num_regimes: int):
        super().__init__()
        self.state_dim = state_dim
        self.input_dim = input_dim
        self.num_regimes = num_regimes

        self.update_net = nn.Sequential(
            nn.Linear(state_dim + input_dim + num_regimes, 256),
            nn.Tanh(),
            nn.Linear(256, state_dim)
        )

    def forward(self, h_prev: torch.Tensor, x_t: torch.Tensor, z_t: torch.Tensor):
        """
        h_prev: (batch, state_dim)
        x_t: (batch, input_dim)
        z_t: (batch,) long regime indices
        """
        batch_size = h_prev.size(0)
        z_onehot = torch.zeros(batch_size, self.num_regimes, device=h_prev.device)
        z_onehot.scatter_(1, z_t.unsqueeze(1), 1.0)

        inp = torch.cat([h_prev, x_t, z_onehot], dim=-1)
        h_t = self.update_net(inp)
        return h_t

