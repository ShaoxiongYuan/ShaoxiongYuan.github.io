@torch.no_grad()
def encode_text_batch(text_list):
    """
    text_list: list of strings
    returns: (batch, embed_dim) embeddings
    """
    enc = enc_tokenizer(
        text_list,
        return_tensors="pt",
        truncation=True,
        padding=True
    ).to(device)

    outputs = enc_model(**enc)
    # Mean-pool over tokens
    pooled = outputs.last_hidden_state.mean(dim=1)  # (batch, hidden)
    return pooled

