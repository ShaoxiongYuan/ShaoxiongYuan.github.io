def generate_message(step, conv_id):
    # Just to have some variation in text
    templates = [
        "Conversation {c}, step {s}: please help me understand neural networks.",
        "In convo {c}, at turn {s}, I still don't get backpropagation.",
        "Convo {c} / turn {s}: can you clarify this concept?",
        "We are at step {s} of conversation {c}. Explain more about AI.",
        "Convo {c}, step {s}: now I just want a short recap."
    ]
    t = random.choice(templates)
    return t.format(c=conv_id, s=step)

def true_regime(step):
    if step == 0:
        return 0  # EXPLAINER
    elif step in [1, 2]:
        return 1  # TUTOR
    else:
        return 2  # CONCISE

