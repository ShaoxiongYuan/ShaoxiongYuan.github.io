def predict_regime_for_turn(ssm_model, prev_h, x_t):
    """
    prev_h: (1, state_dim) or None
    x_t: (1, embed_dim)
    returns: regime_idx (int), new_h (1, state_dim)
    """
    with torch.no_grad():
        if prev_h is None:
            h = ssm_model.h0.unsqueeze(0).to(device)
        else:
            h = prev_h

        logits = ssm_model.controller(h, x_t)   # (1, num_regimes)
        regime_idx = logits.argmax(dim=-1)      # (1,)
        z_t = regime_idx                        # teacher forcing at inference is fine here

        new_h = ssm_model.ssm(h, x_t, z_t)      # (1, state_dim)

    return regime_idx.item(), new_h


def hybrid_chat():
    history = []
    h_state = None

    print("Hybrid SSM +", LLM_NAME, "chatbot. Type 'quit' to stop.\n")

    while True:
        user_msg = input("You: ")
        if user_msg.strip().lower() in ["quit", "exit", "q"]:
            break

        # 1) embed user message using the frozen encoder
        x_t = encode_text_batch([user_msg]).to(device)   # (1, embed_dim)

        # 2) predict regime using trained SSM model
        regime_idx, h_state = predict_regime_for_turn(ssm_model, h_state, x_t)

        # 3) generate assistant response with the chat LLM
        response = generate_chat_response(
            history=history,
            user_message=user_msg,
            regime_idx=regime_idx,
            max_new_tokens=128,
            temperature=0.7,
        )

        history.append(("user", user_msg))
        history.append(("assistant", response))

        print(f"[Regime = {regime_idx} -> {REGIME_NAMES[regime_idx]}]")
        print("Assistant:", response)
        print("-" * 60)

