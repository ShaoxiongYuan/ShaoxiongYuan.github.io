class BaselineRegimeClassifier(nn.Module):
    def __init__(self, input_dim: int, num_regimes: int):
        super().__init__()
        self.linear = nn.Linear(input_dim, num_regimes)

    def forward(self, x):
        # x: (batch, embed_dim)
        return self.linear(x)

def train_baseline(
    train_embs, train_regimes,
    test_embs, test_regimes,
    num_epochs=15,
    lr=1e-3
):
    model = BaselineRegimeClassifier(embed_dim, NUM_REGIMES).to(device)
    opt = optim.Adam(model.parameters(), lr=lr)
    crit = nn.CrossEntropyLoss()

    history = {"train_loss": [], "test_acc": []}

    for epoch in range(num_epochs):
        model.train()
        # Flatten sequences into (N*T, dim)
        x_train = train_embs.view(-1, embed_dim).to(device)
        y_train = train_regimes.view(-1).to(device)

        opt.zero_grad()
        logits = model(x_train)
        loss = crit(logits, y_train)
        loss.backward()
        opt.step()

        # Eval
        model.eval()
        with torch.no_grad():
            x_test = test_embs.view(-1, embed_dim).to(device)
            y_test = test_regimes.view(-1).to(device)
            logits_test = model(x_test)
            preds = logits_test.argmax(dim=-1)
            acc = (preds == y_test).float().mean().item()

        history["train_loss"].append(loss.item())
        history["test_acc"].append(acc)

        print(f"[Baseline] Epoch {epoch+1}/{num_epochs} - "
              f"loss={loss.item():.4f} - test_acc={acc*100:.2f}%")

    return model, history

